function displaytransition(filteredKids = transition) {
  const container = document.getElementById("transition-container");
  // const noResults = document.getElementById("no-results");
  container.innerHTML = "";

  const nameBox = document.getElementById("student-name");
  const classBox = document.getElementById("student-class");
  const quoteBox = document.getElementById("student-quote");
  const studentBox = document.getElementById('student-box');
  const cancel = document.getElementById("close");

  // noResults.style.display = 'none';
  // if (filteredKids.length === 0) {
  //   noResults.style.display = "block";
  //   return;
  // }

  filteredKids.forEach(child => {
    const card = document.createElement("div");
    card.className = "class-card";

    const img = document.createElement("img");
    img.className = "class-image";
    img.src = child.image;
    img.alt = child.title;
    img.addEventListener("click", () => {
      nameBox.innerText = child.fullname;
      classBox.innerText = "Class: " + child.Class;
      quoteBox.innerText = "Quote: " + "\"" + child.Description + "\"";
      studentBox.style.display = "block";
    });

    card.innerHTML = `
      <div class="movie-details">
        <div id="kid-name">${child.title}</div>
      </div>
    `;
    card.appendChild(img);
    container.appendChild(card);
  });

  cancel.onclick = function() {
    studentBox.style.display = 'none';
  }

  window.onclick = function(event) {
    if (event.target == studentBox) {
      studentBox.style.display = 'none';
    }
  }
}

function Searchtransition() {
  const input = document.getElementById("searchInput").value.toLowerCase();
  const filtered = transition.filter(child => child.fullname.toLowerCase().includes(input))
  displaytransition(filtered)
}

document.getElementById("searchInput").addEventListener("keyup", Searchtransition);

displaytransition()

