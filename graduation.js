let slideIndex = 0;
const slides = document.querySelectorAll(".group-slide");

function showSlides() {
  slides.forEach((slide, idx) => {
    slide.classList.remove("active");
  });
  slideIndex++;
  if (slideIndex > slides.length) slideIndex = 1;
  slides[slideIndex - 1].classList.add("active");
  setTimeout(showSlides, 4000);
}

window.addEventListener('DOMContentLoaded', showSlides);

function searchStudents() {
  const query = document.getElementById("searchInput").value.toLowerCase();
  const noResults = document.getElementById("no-results");

  // Filter each class
  const filteredBasic5 = Basic5.filter(s => 
    s.title.toLowerCase().includes(query) || s.fullname.toLowerCase().includes(query)
  );
  const filteredJs3 = Js3.filter(s => 
    s.title.toLowerCase().includes(query) || s.fullname.toLowerCase().includes(query)
  );
  const filteredSs3 = Ss3.filter(s => 
    s.title.toLowerCase().includes(query) || s.fullname.toLowerCase().includes(query)
  );

  // Display filtered results
  displayBasic5(filteredBasic5);
  displayJs3(filteredJs3);
  displaySs3(filteredSs3);

  const totalResults = filteredBasic5.length + filteredJs3.length + filteredSs3.length;
  noResults.style.display = totalResults === 0 ? "block" : "none";
}


